
def soln(A):
    mid= 0
    for i in range(len(A)):
        if A[i]>=0:
            mid= i
            break
    i,j,k = mid-1,mid,0
    res=[0 for i in range(len(A))]

    while i>=0 and j<len(A):
        if(A[i]**2<A[j]**2):
            res[k]= A[i]**2
            i-=1
        else:
            res[k]= A[j]**2
            j+=1
        k+=1
    while i>=0:
        res[k]= A[i]**2
        i-=1
        k+=1
    while j<len(A):
        res[k]= A[j]**2
        j+=1
        k+=1
    return res

